// Variaveis declaradas com var
// Usar variaveis var para navegadores antigos
var x = 5;
var y = 10
var t = 13;
var z = x + y + t;
console.log(z);


// Variaveis declaradas com let
// Usar let sempre que os valores poderem variar que não seja o mesmo sempre
let s = 5;
let g = 10
let n = 13;
let m = s + g + n;
console.log(m);


// Variaveis declaradas com const
// Usar const sempre que o valor for continuar o mesmo durante todo o código
const a = 5;
const b = 10
const c = 13;
const d = a + b + c;
console.log(d);


/*

    Identificadores JavaScript
    Todas as variáveis JavaScript devem ser identificadas com nomes exclusivos .

    Esses nomes exclusivos são chamados de identificadores .

    Os identificadores podem ser nomes curtos (como x e y) ou nomes mais descritivos (age, sum, totalVolume).

    As regras gerais para a construção de nomes para variáveis (identificadores exclusivos) são:

    Os nomes podem conter letras, dígitos, sublinhados e cifrões.
    Os nomes devem começar com uma letra.
    Os nomes também podem começar com $ e _ (mas não o usaremos neste tutorial).
    Os nomes diferenciam maiúsculas de minúsculas (y e Y são variáveis diferentes).
    Palavras reservadas (como palavras-chave JavaScript) não podem ser usadas como nomes.

*/