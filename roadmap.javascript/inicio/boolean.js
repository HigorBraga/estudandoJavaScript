/*

A Função Booleana()
Você pode usar a Boolean()função para descobrir se uma expressão (ou uma variável) é verdadeira.

*/

console.log(Boolean(5 > 2));

//  == Igual
// > maior que
// < menor que
// != negação
// >= maior ou igual
// <= menor ou igual
// === igualdade restrita

// O valor booleano de uma expressão é a base para todas as comparações e condições de JavaScript.